import UIKit


extension UIColor {
    
    class func turquoiseColor() -> UIColor {
        return UIColor(red:26/255.0, green:188/255.0, blue:156/255.0, alpha:0.3);
    }
    class func peterriverColor() -> UIColor {
        return UIColor(red:52/255.0, green:152/255.0, blue:219/255.0, alpha:0.3);
    }
    class func sunflowerColor() -> UIColor {
        return UIColor(red:241/255.0, green:196/255.0, blue:15/255.0, alpha:0.3);
    }
}